#include <stdio.h>

/**
 * add - Return the addition of two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: Sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Return the subtraction of two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: Difference between a and b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Return the multiplication of up two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: Product of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Return the division two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: division of a by b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Return the modulus of two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: mod of a and b
 */
int mod(int a, int b)
{
	return (a % b);
}
